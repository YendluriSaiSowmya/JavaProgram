package com.dest.basics.javaprogram;

public class Operators {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 30;
		int res1 = a+b;
		System.out.println("Addition result:"+ res1);
		int res2 = c-a;
		System.out.println("subraction result:"+res2);
		int res3 = a*b;
		System.out.println("multiplication result:"+res3);
		int res4 = a/c;
		System.out.println("division result:"+res4);
		int res5 = a%c;
		System.out.println("modulus result:"+res5);
		
		




				

	}

}
